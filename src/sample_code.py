def add(self, x, y):
    """値Xと値Yの和を求める

    Args:
        x (int): 値X
        y (int): 値Y

    Returns:
        int: 値Xと値Yの和
    """
    return x + y

def diff(self, x, y):
    """値Xと値Yの差を求める

    Args:
        x (int): 値X
        y (int): 値Y

    Returns:
        int: 値Xと値Yの差
    """
    return x - y